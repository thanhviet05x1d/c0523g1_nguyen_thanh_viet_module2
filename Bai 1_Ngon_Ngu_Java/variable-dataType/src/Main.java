public class Main {
    public static void main(String[] args) {
        int i = 10;//Khai báo biến i là Integer có giá trị bằng 10
        float f = 20.5f;//Khai báo biến f kiểu float có giá trị bằng 20.5
        double d = 20.5;//Khai báo biến d kiểu double có giá trị bằng 20.5
        boolean b = true;//Khai báo biến b kiểu boolean có giá trị bằng true
        char a='a'; // Khai báo biến a có kiểu char giá trị là ký tự a
        String s="Hà Nội"; // Khai báo biến s kiểu String có giá trị là chữ Hà Nội
        System.out.println("i = "+i);
        System.out.println("f = "+f);
        System.out.println("d = "+d);
        System.out.println("b = "+b);
        System.out.println("a = "+a);
        System.out.println("s = "+s);
    }
}
